package Statements;

public class SwitchStatementDemo {

	public static void main(String args[]) 
	{
		int num=10;
		
		//Switch case using INTEGER
		
		switch(num)
		{
		case 10:
			System.out.println("10");
			break;
			
		case 20: 
			System.out.println("Case 20 is execcuted");
			break;
		
		default:
			System.out.println("Default");
	
		
		}
	}

}
