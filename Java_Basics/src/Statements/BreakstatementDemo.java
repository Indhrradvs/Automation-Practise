package Statements;

public class BreakstatementDemo {

	public static void main(String[] args) 
	{
		for(int i=1;i<=10;i++)
		{
			if(i==5)
			{
				break;
			}
		System.out.println(i);
		}
		
	}

}

/* you are saying that BREAK 5th iteration,
	so that only 4 iterations are done */