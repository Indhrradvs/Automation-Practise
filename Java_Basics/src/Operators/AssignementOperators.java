package Operators;

public class AssignementOperators {

	public static void main(String[] args) 
	{
		int a=10;
		int b=5;
		
		// EQUALTO operator
		
		b=a;
		System.out.println("= output is"+ b);
		
		
		// +EQUALTO operator b=b+a
		
		b +=a; 
		System.out.println("+ output is"+ b);
		
		// -EQUALTO operator
		
		b -=a;
		System.out.println(" - output is"+ b);
		
		// *EQUALTO operator
		
		b *=a;
		System.out.println("* output is"+ b);
		
		

	}

}
